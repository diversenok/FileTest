static LPCTSTR GetNtStatusString(NTSTATUS NtStatus)
{
    PNTSTATUS_LINEAR_ENTRY pStartEntry = LinearList;
    PNTSTATUS_LINEAR_ENTRY pMidlEntry;
    PNTSTATUS_LINEAR_ENTRY pEndEntry = pStartEntry + _countof(LinearList);

    // Perform binary search on the table
    while(pStartEntry < pEndEntry)
    {
        // Calculate the middle of the interval
        pMidlEntry = pStartEntry + ((pEndEntry - pStartEntry) / 2);

        // Did we find it?
        if(pMidlEntry->Status == NtStatus)
            return pMidlEntry->StatusString;

        // Move the interval to the left or right
        ((DWORD)NtStatus < (DWORD)pMidlEntry->Status) ? pEndEntry = pMidlEntry : pStartEntry = pMidlEntry + 1;
    }

    // Not found
    return NULL;
}

//-----------------------------------------------------------------------------
// Public functions

LPCTSTR NtStatus2Text(NTSTATUS Status)
{
    LPCTSTR szStr;
    static TCHAR szStatus[64];

    // Get the name of the NTSTATUS. If failed, print it as hexa value
    // Note that this is not thread-safe !!!
    szStr = GetNtStatusString(Status);
    if(szStr == NULL)
    {
        StringCchPrintf(szStatus, _countof(szStatus), _T("%08lX"), Status);
        szStr = szStatus;
    }

    return szStr;
}

